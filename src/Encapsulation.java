
public class Encapsulation {
	private String name;
	private int age;
	private String idNum;

	public int getAge(){
		return age;
	}
	public String getName(){
		return name;
	}
	public String getIdName(){
		return idNum;
	}
	public void setName(String name){
		this.name=name;
	}
	public void setAge(int age){
		this.age=age;
	}
	public void setIdNum(String idNum){
		this.idNum=idNum;
	}
}
